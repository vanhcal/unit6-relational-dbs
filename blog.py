def make_salt(length = 5):
    return ''.join(random.choice(letters) for x in xrange(length))

def make_pw_hash(name, pw, salt = None):
    if not salt:
        salt = make_salt()
    h = hashlib.sha256(name + pw + salt).hexdigest()
    return '%s,%s' % (salt, h)

def valid_pw(name, password, h):
    salt = h.split(',')[0]
    return h == make_pw_hash(name, password, salt)

# not required, creates "ancestor element" in db to store all users, and creates groups for them to potentially be put into in the future
def users_key(group = 'default'):
    return db.Key.from_path('users', group)

# the user object that we'll be storing in the db
class User(db.Model):
    name = db.StringProperty(required = True)
    # remember that we're storing the pw hash, not the pw itself
    pw_hash = db.StringProperty(required = True)
    email = db.StringProperty()

    # classmethod means that these methods below are being called on the class User, rather than on INSTANCES of users
    # by_id calls a user by id, and uses id number to get them out of the database
    @classmethod
    def by_id(cls, uid):
        return User.get_by_id(uid, parent = users_key())

    # look up a user by their name
    @classmethod
    def by_name(cls, name):
        u = User.all().filter('name =', name).get()
        return u

    # takes name, pw, email and creates a new user object (but does not yet store it in the db)
    @classmethod
    def register(cls, name, pw, email = None):
        # create the pw hash for this user
        pw_hash = make_pw_hash(name, pw)
        # parent here is not required
        return User(parent = users_key(),
                    name = name,
                    pw_hash = pw_hash,
                    email = email)

    @classmethod
    def login(cls, name, pw):
        # refers to user class
        # both the login and register functions set cookies
        u = cls.by_name(name)
        if u and valid_pw(name, pw, u.pw_hash):
            return u

